package server;

public class Message {

    public String date;
    public String time;
    public String login;
    public String type;
    public String mes;

    public Message(String date, String time, String login, String type, String mes) {
        this.date  = date;
        this.time  = time;
        this.login = login;
        this.type  = type;
        this.mes   = mes;
    }



}
